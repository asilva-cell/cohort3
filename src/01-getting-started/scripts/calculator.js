

const calculations = {
    add: (num1, num2) => {
        return num1 + num2;
    },

    subtract: (num1, num2) => {
        return num1 - num2;
    },

    multiplication: (num1, num2) => {
        return num1 * num2;
    },

    division: (num1, num2) => {
        return num1 / num2;
    }
    
};



export default calculations;
